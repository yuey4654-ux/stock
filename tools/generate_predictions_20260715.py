def append(path, picks, hk=False):
    with path.open(encoding='utf-8-sig',newline='') as f: rows=list(csv.DictReader(f));fields=list(rows[0])
    rows=[r for r in rows if r.get('目标日期')!=TARGET]
    for x in picks:
        row={k:'' for k in fields}; row.update({'预测日期':PRED,'目标日期':TARGET,**x,'收盘价':'','涨跌幅':'','是否触发':'','是否失效':'','复盘结果':'待复盘','复盘备注':f'{ISO}全量预测样本；新闻只决定优先级，必须执行回踩、失效位和高开不追规则。'})
        if hk: row['是否计入准确率']='是'
        rows.append(row)
    with path.open('w',encoding='utf-8-sig',newline='') as f:w=csv.DictWriter(f,fieldnames=fields,quoting=csv.QUOTE_ALL);w.writeheader();w.writerows(rows)

