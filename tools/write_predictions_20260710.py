def upsert_predictions(path: Path, rows: list[dict], fields: list[str]) -> None:
    existing = [normalize(row, fields) for row in read_csv(path)]
    existing = [
        row
        for row in existing
        if not (row.get("预测日期") == PREDICTION_DATE and row.get("目标日期") == TARGET_DATE)
    ]
    write_csv(path, existing + [normalize(row, fields) for row in rows], fields)


def copy_outputs(a_md: Path, a_table: Path, a_report: Path, hk_md: Path, hk_report: Path) -> None:
    if not WORKBENCH.exists():
        return
    pairs = [
        (A_LEDGER, WORKBENCH / "03_每日预测台账.csv"),
        (a_md, WORKBENCH / f"02_明日预测票_{TARGET_DASH}.md"),
        (a_table, WORKBENCH / f"02A_明日预测票_表格版_{TARGET_DASH}.md"),
        (a_report, WORKBENCH / "11_每日热点预测.md"),
        (HK_LEDGER, WORKBENCH / "03A_港股预测台账.csv"),
        (hk_md, WORKBENCH / f"02B_港股明日预测票_{TARGET_DASH}.md"),
        (hk_report, WORKBENCH / "11A_港股每日热点预测.md"),
    ]
    for src, dst in pairs:
        shutil.copyfile(src, dst)


