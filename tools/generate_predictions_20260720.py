def upsert(path, picks, hk=False):
    with path.open(encoding="utf-8-sig", newline="") as f:
        rows = list(csv.DictReader(f))
        fields = list(rows[0])
    rows = [r for r in rows if r["目标日期"] != TD]
    for rank, code, name, kind, logic, trigger, invalid in picks:
        row = {k: "" for k in fields}
        row.update({
            "预测日期": PD,
            "目标日期": TD,
            "排名": rank,
            "代码": code,
            "名称": name,
            "市场": "港股" if hk else "A股",
            "预测类型": kind,
            "预测逻辑": logic,
            "触发条件": trigger,
            "失效条件": invalid,
            "复盘结果": "待复盘",
            "复盘备注": f"{ISO} V3全量预测样本；价格达到观察区不等于自动成交，必须执行承接、尾盘确认和高开不追。",
        })
        if hk and "是否计入准确率" in row:
            row["是否计入准确率"] = "是"
        rows.append(row)
    with path.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fields, quoting=csv.QUOTE_ALL)
        writer.writeheader()
        writer.writerows(rows)


def table(picks):
    return "\n".join(
        f"| {x[0]} | {x[2]} `{x[1]}` | {x[3]} | {x[5]} | {x[6]} |" for x in picks
    )


